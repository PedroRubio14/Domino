public class Ficha {
    private int ladoIz;
    private int ladoDe;

    public Ficha(int ladoIz, int ladoDe) {
        this.ladoIz = ladoIz;
        this.ladoDe = ladoDe;
    }

    public int getLadoIz() {
        return ladoIz;
    }

    public void setLadoIz(int ladoIz) {
        this.ladoIz = ladoIz;
    }

    public int getLadoDe() {
        return ladoDe;
    }

    public void setLadoDe(int ladoDe) {
        this.ladoDe = ladoDe;
    }
}
